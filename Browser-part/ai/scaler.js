const MEANS = [
  0.4450468094580835, 0.5428938124450018, 0.36956685128245753,
  0.5487147606167053, 0.42771374189372896, 0.4189660389745779,
];

const STDS = [
  0.27062680698895175, 0.2631600053910696, 0.269822823350004,
  0.2227479536605272, 0.3564721093356065, 0.2778712683717434,
];

export function scale(features) {
  return features.map((v, i) => (v - MEANS[i]) / STDS[i]);
}
